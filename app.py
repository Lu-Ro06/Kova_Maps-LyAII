    min_lat = min(p['lat'] for p in ruta) - 0.05
    max_lat = max(p['lat'] for p in ruta) + 0.05
    min_lon = min(p['lon'] for p in ruta) - 0.05
    max_lon = max(p['lon'] for p in ruta) + 0.05
    
    casetas_candidatas = [c for c in casetas if min_lat <= c['lat'] <= max_lat and min_lon <= c['lon'] <= max_lon]
    
    # 1. Casetas
    costo_total_casetas = 0
    casetas_cruzadas = []
    for i in range(len(ruta) - 1):
        inicio = (ruta[i]['lat'], ruta[i]['lon'])
        fin = (ruta[i+1]['lat'], ruta[i+1]['lon'])
        for c in casetas_candidatas:
            caseta_punto = (c['lat'], c['lon'])
            if punto_cerca_de_segmento(caseta_punto, inicio, fin):
                # Extraemos el costo y nombre exacto asignado a esta caseta
                costo = c.get('costo', 135)
                nombre = c.get('nombre', 'Caseta')
                
                # Evitar duplicados si la caseta se detecta en multiples segmentos
                # O si hay otra caseta a menos de 2 km (misma plaza de cobro en OSM)
                duplicada = False
                for ya_cruzada in casetas_cruzadas:
                    if ya_cruzada['nombre'] == nombre and ya_cruzada['lat'] == c['lat']:
                        duplicada = True
                        break
                    # Distancia espacial para evitar dobles cobros en la misma plaza
                    dist = distancia_manhattan(caseta_punto, (ya_cruzada['lat'], ya_cruzada['lon']))
                    if dist < 2.0:
                        duplicada = True
                        break
                
                if not duplicada:
                    costo_total_casetas += costo
                    casetas_cruzadas.append({"nombre": nombre, "costo": costo, "lat": c['lat'], "lon": c['lon']}) 

    # 2. Zonas Críticas
    zonas_detectadas = []
    zonas_criticas_candidatas = [z for z in zonas_criticas if min_lat <= z['lat'] <= max_lat and min_lon <= z['lon'] <= max_lon]
    
