def obtener_desnivel_acumulado(ruta_muestra):
    """
    Usa la API pública Open-Elevation para calcular la subida acumulada.
    Toma una muestra de la ruta para no sobrecargar.
    """
    if not ruta_muestra:
        return 0
    # Limitar la muestra a 20 puntos máximo para no tardar mucho
    paso = max(1, len(ruta_muestra) // 20)
    puntos = ruta_muestra[::paso]
    
    locations = "|".join([f"{p['lat']},{p['lon']}" for p in puntos])
    try:
        url = f"https://api.open-elevation.com/api/v1/lookup?locations={locations}"
        response = requests.get(url, timeout=3)
        data = response.json()
        elevaciones = [res['elevation'] for res in data['results']]
        
        desnivel_positivo = 0
        for i in range(len(elevaciones) - 1):
            dif = elevaciones[i+1] - elevaciones[i]
            if dif > 0:
                desnivel_positivo += dif
        return desnivel_positivo
    except Exception as e:
        print("Error al obtener elevación:", e)
        return 0 # Si falla la API, retornamos 0 para no romper la app

def distancia_manhattan(p1, p2):
    """Calcula distancia Manhattan aproximada en km entre dos puntos GPS."""
    lat1, lon1 = p1
    lat2, lon2 = p2
    km_lat = abs(lat1 - lat2) * 111
    km_lon = abs(lon1 - lon2) * 111 * cos(radians((lat1+lat2)/2))
    return km_lat + km_lon

def punto_cerca_de_segmento(p, seg_inicio, seg_fin, umbral_km=0.5):
    """
    Verifica si el punto p está dentro de umbral_km de distancia Manhattan a 
    alguno de los segmentos de línea (seg_inicio -> seg_fin)
    """
    dist_inicio = distancia_manhattan(p, seg_inicio)
    dist_fin = distancia_manhattan(p, seg_fin)
    return dist_inicio <= umbral_km or dist_fin <= umbral_km

